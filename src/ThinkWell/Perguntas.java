    private void AddPergunta() {
        try {
            dao = new PerguntasDAO();
            Pergunta = dao.selecionar();
            txtPergunta.setText(Pergunta);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }

    private void AddRespostas() {
        List respE = new ArrayList();
        try {
            RespostasDAO re = new RespostasDAO();
            respC = re.RespC(dao);
            respE = re.RespE(dao);

            respostas.add(respC);
            respostas.add(respE.get(0));
            respostas.add(respE.get(1));
            respostas.add(respE.get(2));
            respostas.add(respE.get(3));

            Collections.shuffle(respostas);

            rbtAlt1.setText((String) respostas.get(0));
            rbtAlt2.setText((String) respostas.get(1));
            rbtAlt3.setText((String) respostas.get(2));
            rbtAlt4.setText((String) respostas.get(3));
            rbtAlt5.setText((String) respostas.get(4));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

    private boolean botaoSelec() {
        boolean se = false;

        if (rbtAlt1.isSelected()) {
            btnSelecionado = rbtAlt1.getText();
            se = true;
        } else if (rbtAlt2.isSelected()) {
            btnSelecionado = rbtAlt2.getText();
            se = true;
        } else if (rbtAlt3.isSelected()) {
            btnSelecionado = rbtAlt3.getText();
            se = true;
        } else if (rbtAlt4.isSelected()) {
            btnSelecionado = rbtAlt4.getText();
            se = true;
        } else if (rbtAlt5.isSelected()) {
            btnSelecionado = rbtAlt5.getText();
            se = true;
        }
        return se;
    }

    public boolean errouAcertou() {
        boolean re;

        if (btnSelecionado.equals(respC)) {
            re = true;
        }else {
            if (respC.equals(rbtAlt1.getText())) {
                altC = "A";
            }else if (respC.equals(rbtAlt2.getText())) {
                altC = "B";
            }else if (respC.equals(rbtAlt3.getText())) {
                altC = "C";
            }else if (respC.equals(rbtAlt4.getText())) {
                altC = "D";
            }else if (respC.equals(rbtAlt5.getText())) {
                altC = "E";
            }
            re = false;
        }
        return re;
    }

